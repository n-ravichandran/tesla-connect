import SwiftUI

extension Color {

    enum Fill {
        static let tileBackground = Color("tile")
        static let commandButton = Color("commandButtonBackground")
    }

    enum Text {
        static let commandButtonText = Color("commandButtonText")
    }

    enum Tint {
        static let navigationBar = Color("navigationBarTint")
    }

}
